package com.example.androidd;

public class YourDataModel {
    private int id;
    private int reportTimeCount;
    private String date;
    private String reportTimeText;

    // Constructors, getters, and setters...

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getReportTimeCount() {
        return reportTimeCount;
    }

    public void setReportTimeCount(int reportTimeCount) {
        this.reportTimeCount = reportTimeCount;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getReportTimeText() {
        return reportTimeText;
    }

    public void setReportTimeText(String reportTimeText) {
        this.reportTimeText = reportTimeText;
    }
}
